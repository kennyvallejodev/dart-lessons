/**
 * The `main` function will be our unique 'entry point of execution'
 * into our program 
 * -  
 * La funcion `main` sera nuestro unico `punto de entrada en ejecucion`
 * dentro de nuestro programa
 */
void main() {
  /**
   * `print` function behaves equals to `console.log` in js 
   * -  
   * la funcion `print` se comporta exactamente igual que 
   * `console.log` en javascript
   */
  print('Hello World');
  /**
   * In dart, we need to finish our sentences with a semicolon (;) 
   *  -  
   * En dart, necesitamos finalizar nuestras sentencias con un
   * punto y coma (;)
   */
}