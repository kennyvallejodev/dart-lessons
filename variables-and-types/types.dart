void main() {
  /**
   * Los Types Primitivos en Dart son Los Siguientes:
   * 1. String
   * 2. int
   * 3. double
   * 4. bool
   * 
   */

  String myStringVal = 'This is myStringVal';
  int myIntVal = 1;
  double myDoubleVal = 1.15;
  bool myBoolVal = true;

  print('[String] myStringVal Output => ${myStringVal}');
  print('[int] myIntVal Output => ${myIntVal}');
  print('[double] myDoubleVal Output => ${myDoubleVal}');
  print('[myBoolVal] myBoolVal Output => ${myBoolVal}');
}
